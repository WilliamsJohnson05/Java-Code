class bitwiseoperator {
    public static void main(String args[]){

        int a = 5 ,b= 2;
        int c = a&b;   // convert the value in binary digit, and then do &,|, ^ operation 
        int d = a>>b;  // double right shift is used for multiply twice
        System.out.println(c);
        System.out.println(d);
    }}
    