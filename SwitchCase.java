package OpenLabBook;
public class SwitchCase {
	

public static void main(String args[]){
 int Tech = 3;

 switch(Tech){
case 1:
 {
 System.out.println("Smart Phone.....");
 break;
 }
 case 2:{
 System.out.println("Laptops.....");
 break;
 }
 default:{
 System.out.println("Airpods....");
    }
 }
}
}
 

