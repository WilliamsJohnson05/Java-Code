package CloseLabBook;

public class CommentDocumentation {
	/**
	 * This is a sample Java program with comment documentation.
	 * It demonstrates different ways to define the main method and its execution.
	 */

	    
	    public static void main(String[] args) {
	        System.out.println("Executing main method with public and static specifiers: ");
	    }

	    /**
	     * This is the main method without the public specifier. It cannot be executed directly from the command line.
	     * It prints a message to the console.
	     */
	     
	   public static void mainWithoutPublicSpecifier(String[] args) {
	        System.out.println("Executing main method without the public specifier: ");
	    }

	    /**
	     * This is the main method without the static modifier. It cannot be executed directly from the command line.
	     * It prints a message to the console.
	     */
	     
	    public void mainWithoutStaticModifier(String[] args) {
	        System.out.println("Executing main method without the static modifier: ");
	    }

	    /**
	     * This is the main method without function parameters. It cannot be executed directly from the command line.
	     * It prints a message to the console.
	     */
	    public static void mainWithoutFunctionParameter() {
	        System.out.println("Executing main method without function parameters: ");
	    }
	}





